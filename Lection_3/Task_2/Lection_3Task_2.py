def draw_diamond(n):
    # Рисуем верхнюю половину ромба
    for i in range(1, n+1):
        print(" " * (n-i) + "*" * (2*i-1))
    
    # Рисуем нижнюю половину ромба
    for i in range(n-1, 0, -1):
        print(" " * (n-i) + "*" * (2*i-1))

# Вызываем функцию с n=5, чтобы нарисовать полный ромб
draw_diamond(5)